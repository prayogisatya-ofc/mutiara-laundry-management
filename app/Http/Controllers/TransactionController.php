    public function index(Request $request) {
        $search = $request->input('search');

        $data = Transaction::query();

        if ($search) {
            $data->where('name', 'like', '%'.$search.'%');
        }

        $data = $data->latest()->paginate(10);

        return view('transactions.list', [
            'title' => 'Data Transaksi',
            'data' => $data,
            'search' => $search
        ]);
    }

